package com.Day1;

public class Day1 {
	public int a;
	public String b;
	public double c;

	public Day1(int a, String b, double c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		
	}

	public Day1() {
		super();
	
		
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}
 
	public double getC() {
		return c;
	}

	public void setC(double c) {
		this.c = c;
	}

	
}
